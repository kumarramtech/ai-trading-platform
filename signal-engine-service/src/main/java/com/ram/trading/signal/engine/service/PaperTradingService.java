    public void createTrade(
            TradingSignalEntity signal,
            TechnicalIndicatorResponse indicatorResponse) {

        if (indicatorResponse == null) {
            throw new RuntimeException(
                    "Technical indicators not found for "
                            + signal.getSymbol());
        }

        log.info("Signal Generated = {}", signal.getSignal());

        // Skip HOLD signals immediately
        if (SignalType.HOLD.name().equals(signal.getSignal())) {

            log.info("HOLD Signal. Trade creation skipped.");

            return;
        }

        // Prevent duplicate OPEN trades
        PaperTrade existingTrade =
                repository
                        .findTopBySymbolAndStatusOrderByEntryTimeDesc(
                                signal.getSymbol(),
                                SignalStatus.OPEN)
                        .orElse(null);

        if (existingTrade != null) {

            log.info("======================================");
            log.info("Open Trade Already Exists");
            log.info("Skipping Trade Creation");
            log.info("Symbol : {}", signal.getSymbol());
            log.info("Trade Id : {}", existingTrade.getId());
            log.info("======================================");

            return;
        }

        log.info("Creating New Paper Trade...");

        int quantity =
                (int) (capitalPerTrade
                        / signal.getEntryPrice());

        log.info("quantity ="+quantity);
        if (quantity <= 0) {
            return;
        }
        log.info("Confidence = {}", signal.getConfidence());

        log.info(
                "Creating Trade => Entry={}, Target={}, Stop={}",
                signal.getEntryPrice(),
                signal.getTargetPrice(),
                signal.getStopLoss());

// ======================================================
// BUSINESS VALIDATION
// ======================================================

        if ("BUY".equalsIgnoreCase(signal.getSignal())) {

            if (signal.getTargetPrice() <= signal.getEntryPrice()) {

                log.error(
                        "Invalid Trade Generated by AI. Symbol={}, Signal={}, Entry={}, Target={}, Stop={}",
                        signal.getSymbol(),
                        signal.getSignal(),
                        signal.getEntryPrice(),
                        signal.getTargetPrice(),
                        signal.getStopLoss());

                throw new IllegalStateException(
                        "Invalid BUY Trade : Target Price must be greater than Entry Price.");
            }

            if (signal.getStopLoss() >= signal.getEntryPrice()) {

                log.error(
                        "Invalid Trade Generated by AI. Symbol={}, Signal={}, Entry={}, Target={}, Stop={}",
                        signal.getSymbol(),
                        signal.getSignal(),
                        signal.getEntryPrice(),
                        signal.getTargetPrice(),
                        signal.getStopLoss());

                throw new IllegalStateException(
                        "Invalid BUY Trade : Stop Loss must be less than Entry Price.");
            }

        }
        else if ("SELL".equalsIgnoreCase(signal.getSignal())) {

            if (signal.getTargetPrice() >= signal.getEntryPrice()) {

                log.error(
                        "Invalid Trade Generated by AI. Symbol={}, Signal={}, Entry={}, Target={}, Stop={}",
                        signal.getSymbol(),
                        signal.getSignal(),
                        signal.getEntryPrice(),
                        signal.getTargetPrice(),
                        signal.getStopLoss());

                throw new IllegalStateException(
                        "Invalid SELL Trade : Target Price must be less than Entry Price.");
            }

            if (signal.getStopLoss() <= signal.getEntryPrice()) {

                log.error("Invalid Trade Generated by AI. Symbol={}, Signal={}, Entry={}, Target={}, Stop={}",
                        signal.getSymbol(),
                        signal.getSignal(),
                        signal.getEntryPrice(),
                        signal.getTargetPrice(),
                        signal.getStopLoss());

                throw new IllegalStateException("Invalid SELL Trade : Stop Loss must be greater than Entry Price.");
            }
        }

// ======================================================
// CREATE PAPER TRADE
// ======================================================

        PaperTrade trade =
                PaperTrade.builder()
                        .symbol(signal.getSymbol())
                        .signalId(signal.getId())
                        .signal(signal.getSignal())
                        .entryPrice(round(signal.getEntryPrice()))
                        .quantity(quantity)
                        .investedAmount(
                                quantity *
                                        signal.getEntryPrice())
                        .rsi(indicatorResponse.getRsi14())
                        .ema20(indicatorResponse.getEma20())
                        .ema50(indicatorResponse.getEma50())
                        .macd(indicatorResponse.getMacd())
                        .targetPrice(round(signal.getTargetPrice()))
                        .stopLoss(round(signal.getStopLoss()))
                        .status(SignalStatus.OPEN)
                        .confidence(signal.getConfidence())
                        .entryTime(LocalDateTime.now())
                        .build();
        PaperTrade saved =
                repository.save(trade);

        portfolioClient
                .openPosition(
                        saved.getSymbol(),
                        saved.getQuantity(),
                        saved.getEntryPrice())
                .doOnSuccess(v ->
                        log.info("Portfolio Updated Successfully : {}",
                                saved.getSymbol()))
                .onErrorResume(ex -> {
                    log.error("Portfolio Update Failed", ex);
                    return Mono.empty();
                })
                .subscribe();

        log.info(
                "Saved Trade => target={}, stop={}",
                saved.getTargetPrice(),
                saved.getStopLoss());

        NotificationRequest request =
                NotificationRequest.builder()
                        .channel(NotificationChannel.SLACK)
                        .title("TRADE OPENED")
                        .message(
                                "Symbol: " + signal.getSymbol()
                                        + ", Signal: " + signal.getSignal()
                                        + ", Entry: " + signal.getEntryPrice()
                                        + ", Target: " + signal.getTargetPrice()
                                        + ", StopLoss: " + signal.getStopLoss()
                                        + ", PnL : ₹" + signal.getProfitLoss()
                                        + ", Confidence: "
                                        + signal.getConfidence())
                        .build();

        notificationClient
                .sendNotification(request)
                .doOnError(e ->
                        log.error("Slack Notification Failed", e))
                .subscribe();
    }


