    public Mono<OpenPositionContextResponse> getOpenPositionContext(String symbol) {

        PaperTrade trade = repository
                .findTopBySymbolAndStatusOrderByEntryTimeDesc(
                        symbol,
                        SignalStatus.OPEN)
                .orElse(null);

        if (trade == null) {
            return Mono.just(OpenPositionContextResponse.builder()
                            .positionExists(false)
                            .symbol(symbol)
                            .build());
        }

        return stockServiceClient
                .getStockPrice(symbol)
                .map(stock -> {

                    Double currentPrice = stock.getPrice();

                    Double currentPnL;

                    if ("BUY".equalsIgnoreCase(trade.getSignal())) {

                        currentPnL =
                                (currentPrice - trade.getEntryPrice())
                                        * trade.getQuantity();

                    } else {

                        currentPnL =
                                (trade.getEntryPrice() - currentPrice)
                                        * trade.getQuantity();
                    }

                    Double pnlPercentage =
                            (currentPnL / trade.getInvestedAmount()) * 100;

                    return OpenPositionContextResponse.builder()
                            .positionExists(true)
                            .symbol(trade.getSymbol())
                            .quantity(trade.getQuantity())
                            .entryPrice(trade.getEntryPrice())
                            .currentPrice(currentPrice)
                            .currentPnL(Math.round(currentPnL * 100.0) / 100.0)
                            .pnlPercentage(Math.round(pnlPercentage * 100.0) / 100.0)
                            .stopLoss(trade.getStopLoss())
                            .targetPrice(trade.getTargetPrice())
                            .status(trade.getStatus().name())
                            .signal(trade.getSignal())
                            .build();
                });
    }

    public PaperTradeSummary getSummary() {

        List<PaperTrade> trades =
                repository.findAll();

        long totalTrades =
                trades.size();

        long openPositions =
                trades.stream()
                        .filter(t ->
                                SignalStatus.OPEN
                                        .equals(t.getStatus()))
                        .count();

        long closedPositions =
                totalTrades - openPositions;

        long winningTrades =
                trades.stream()
                        .filter(t ->
                                t.getProfitLoss() != null
                                        && t.getProfitLoss() > 0)
                        .count();

        long losingTrades =
                trades.stream()
                        .filter(t ->
                                t.getProfitLoss() != null
                                        && t.getProfitLoss() < 0)
                        .count();

        long breakevenTrades =
                trades.stream()
                        .filter(t ->
                                t.getProfitLoss() != null
                                        && t.getProfitLoss() == 0)
                        .count();

        double totalInvestment =
                trades.stream()
                        .filter(t ->
                                SignalStatus.OPEN
                                        .equals(t.getStatus()))
                        .mapToDouble(
                                PaperTrade::getInvestedAmount)
                        .sum();

        double totalProfit =
                trades.stream()
                        .filter(t ->
                                t.getProfitLoss() != null)
                        .mapToDouble(
                                PaperTrade::getProfitLoss)
                        .sum();

        double winRate =
                (winningTrades + losingTrades) == 0
                        ? 0
                        : (winningTrades * 100.0)
                        / (winningTrades + losingTrades);

        return PaperTradeSummary.builder()
                .totalTrades(totalTrades)
                .openPositions(openPositions)
                .closedPositions(closedPositions)
                .winningTrades(winningTrades)
                .losingTrades(losingTrades)
                .breakevenTrades(breakevenTrades)
                .totalInvestment(totalInvestment)
                .totalProfit(totalProfit)
                .winRate(Math.round(winRate * 100.0) / 100.0)
                .build();
    }

    public void closeTrade(
            Long signalId,
            Double exitPrice,
            SignalStatus status) {

        PaperTrade trade =
                repository
                        .findBySignalIdAndStatus(
                                signalId,
                                SignalStatus.OPEN)
                        .orElse(null);

        if (trade == null) {
            return;
        }

        trade.setExitPrice(exitPrice);

        double profitLoss;

        if (SignalType.SELL.name()
                .equals(trade.getSignal())) {

            profitLoss =
                    (trade.getEntryPrice()
                            - exitPrice)
                            * trade.getQuantity();

        } else {

            profitLoss =
                    (exitPrice
                            - trade.getEntryPrice())
                            * trade.getQuantity();
        }

        trade.setProfitLoss(profitLoss);

        trade.setStatus(status);

        trade.setExitTime(
                LocalDateTime.now());

        log.info("Closing Trade for signalId="  + signalId);

        log.info( "Trade Found="  + trade.getId());

        log.info( "Profit/Loss=" + profitLoss);

        PaperTrade savedTrade =
                repository.save(trade);

        portfolioClient
                .closePosition(
                        savedTrade.getSymbol(),
                        savedTrade.getQuantity())
                .doOnSuccess(v ->
                        log.info("Portfolio Updated Successfully : {}",
                                savedTrade.getSymbol()))
                .onErrorResume(ex -> {
                    log.error("Portfolio Update Failed", ex);
                    return Mono.empty();
                })
                .subscribe();

        log.info("Trade Closed Successfully");
        log.info("Trade Id : {}", savedTrade.getId());

        NotificationRequest request =
                NotificationRequest.builder()
                        .channel(NotificationChannel.SLACK)
                        .title("TRADE CLOSED")
                        .message(
                                "Symbol: " + trade.getSymbol()
                                        + ", Status: " + status
                                        + ", Entry: "
                                        + trade.getEntryPrice()
                                        + ", Exit: "
                                        + exitPrice
                                        + ", PnL: "
                                        + profitLoss)
                        .build();

        notificationClient
                .sendNotification(request)
                .doOnError(error ->
                        log.error(
                                "Failed to send notification",
                                error))
                .subscribe();
    }

