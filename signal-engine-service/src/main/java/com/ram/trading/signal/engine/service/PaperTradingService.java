    public AnalyticsMetricsResponse getAdvancedMetrics() {

        List<PaperTrade> trades =
                repository.findAll()
                        .stream()
                        .filter(t -> t.getProfitLoss() != null)
                        .toList();

        if (trades.isEmpty()) {

            return AnalyticsMetricsResponse.builder()
                    .profitFactor(0)
                    .expectancy(0)
                    .maxDrawdown(0)
                    .consecutiveWins(0)
                    .consecutiveLosses(0)
                    .build();
        }

        double grossProfit =
                trades.stream()
                        .filter(t -> t.getProfitLoss() > 0)
                        .mapToDouble(PaperTrade::getProfitLoss)
                        .sum();

        double grossLoss =
                Math.abs(
                        trades.stream()
                                .filter(t -> t.getProfitLoss() < 0)
                                .mapToDouble(PaperTrade::getProfitLoss)
                                .sum());

        double profitFactor =
                grossLoss == 0
                        ? grossProfit
                        : grossProfit / grossLoss;

        long winningTrades =
                trades.stream()
                        .filter(t -> t.getProfitLoss() > 0)
                        .count();

        long losingTrades =
                trades.stream()
                        .filter(t -> t.getProfitLoss() < 0)
                        .count();

        double avgWin =
                trades.stream()
                        .filter(t -> t.getProfitLoss() > 0)
                        .mapToDouble(PaperTrade::getProfitLoss)
                        .average()
                        .orElse(0);

        double avgLoss =
                Math.abs(
                        trades.stream()
                                .filter(t -> t.getProfitLoss() < 0)
                                .mapToDouble(PaperTrade::getProfitLoss)
                                .average()
                                .orElse(0));

        double totalTrades =
                winningTrades + losingTrades;

        double winRate =
                totalTrades == 0
                        ? 0
                        : winningTrades / totalTrades;

        double lossRate =
                totalTrades == 0
                        ? 0
                        : losingTrades / totalTrades;

        double expectancy =
                (winRate * avgWin)
                        - (lossRate * avgLoss);

        double maxDrawdown =
                TradeUtil.calculateMaxDrawdown(trades);

        long consecutiveWins =
                TradeUtil.calculateConsecutiveWins(trades);

        long consecutiveLosses =
                TradeUtil.calculateConsecutiveLosses(trades);

        return AnalyticsMetricsResponse.builder()
                .profitFactor(TradeUtil.round(profitFactor))
                .expectancy(TradeUtil.round(expectancy))
                .maxDrawdown(TradeUtil.round(maxDrawdown))
                .consecutiveWins(consecutiveWins)
                .consecutiveLosses(consecutiveLosses)
                .build();
    }

